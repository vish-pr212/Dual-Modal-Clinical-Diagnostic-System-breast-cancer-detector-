from sklearn.metrics import roc_curve, auc, precision_score, recall_score, f1_score, confusion_matrix, accuracy_score
import seaborn as sns

def advanced_evaluation(y_true, y_pred, y_prob, model_name):

    acc = accuracy_score(y_true, y_pred)
    prec = precision_score(y_true, y_pred, zero_division=0)
    rec = recall_score(y_true, y_pred, zero_division=0) # Sensitivity
    f1 = f1_score(y_true, y_pred, zero_division=0)


    tn, fp, fn, tp = confusion_matrix(y_true, y_pred).ravel()
    specificity = tn / (tn + fp) if (tn + fp) > 0 else 0


    print(f"--- {model_name} Performance ---")
    print(f"Accuracy:    {acc:.4f}")
    print(f"Precision:   {prec:.4f}")
    print(f"Sensitivity: {rec:.4f}")
    print(f"Specificity: {specificity:.4f}")
    print(f"F1-Score:    {f1:.4f}")
    print("-" * 30)


    plt.figure(figsize=(10, 4))

    # Confusion Matrix
    plt.subplot(1, 2, 1)
    sns.heatmap([[tn, fp], [fn, tp]], annot=True, fmt='d', cmap='Blues')
    plt.title(f'{model_name} Confusion Matrix')

    # ROC Curve
    fpr, tpr, _ = roc_curve(y_true, y_prob)
    roc_auc = auc(fpr, tpr)
    plt.subplot(1, 2, 2)
    plt.plot(fpr, tpr, color='orange', label=f'AUC = {roc_auc:.2f}')
    plt.plot([0, 1], [0, 1], linestyle='--')
    plt.title(f'{model_name} ROC Curve')
    plt.legend()
    plt.show()


