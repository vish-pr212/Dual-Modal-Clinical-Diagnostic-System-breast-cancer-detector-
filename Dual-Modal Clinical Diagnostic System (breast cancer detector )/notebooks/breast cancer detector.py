class BreastCancerSystem:
    def __init__(self, clinical_model, image_model, scaler):
        self.clinical_model = clinical_model
        self.image_model = image_model
        self.scaler = scaler
        print("System Initialized: Ready for Hybrid Diagnosis.")

    def predict_clinical(self, patient_data_array):
        """Predicts clinical outcome and probability."""
        scaled_data = self.scaler.transform([patient_data_array])
        prob = self.clinical_model.predict_proba(scaled_data)[:, 1][0]
        pred = 1 if prob > 0.5 else 0
        return pred, prob

    def analyze_patient_history(self, data_array):
        """Analyzes CSV/Numerical Data and prints formatted output"""
        pred, prob = self.predict_clinical(data_array)
        result = "MALIGNANT" if pred == 1 else "BENIGN"
        print(f"""--- Clinical Analysis ---
Diagnosis: {result}
Risk Probability: {prob:.2%} """)
        return result

    def analyze_mammogram(self, image_path):
        """Analyzes Image Data"""
        # Load and preprocess image
        img = tf.keras.utils.load_img(image_path, target_size=(224, 224))
        img_array = tf.keras.utils.img_to_array(img)
        img_array = np.expand_dims(img_array, axis=0) / 255.0

        prob = self.image_model.predict(img_array, verbose=0)[0][0]
        result = "MALIGNANT" if prob > 0.5 else "BENIGN"

        print(f"--- Imaging Analysis ---\nDiagnosis: {result}\nSeverity Score: {prob:.2%}")
        plt.imshow(img)
        plt.axis('off')
        plt.title(f"Processed Scan: {result}")
        plt.show()
        return result

